/**
 * SHA-2 哈希算法系列模块
 * 
 * SHA-2（Secure Hash Algorithm 2）是一系列密码学哈希函数，由美国国家安全局（NSA）设计，
 * 并于2001年由美国国家标准与技术研究院（NIST）发布。SHA-2包含多个变体，输出长度从224位到512位不等。
 * 
 * SHA-2算法家族包括：
 * - SHA-224：生成224位（28字节）的哈希值
 * - SHA-256：生成256位（32字节）的哈希值，最为常见的SHA-2变体
 * - SHA-384：生成384位（48字节）的哈希值
 * - SHA-512：生成512位（64字节）的哈希值
 * - SHA-512/224：使用SHA-512的变体，但截断输出至224位
 * - SHA-512/256：使用SHA-512的变体，但截断输出至256位
 * 
 * 相比SHA-1，SHA-2算法具有更强的安全性，目前没有已知的有效攻击方法。
 * SHA-256和SHA-512是最常用的变体，广泛应用于TLS、SSL、PGP、SSH、IPsec等安全协议，
 * 以及比特币等区块链技术中。
 * 
 * 本模块导出了所有SHA-2变体的类和便捷函数。
 */

// 导出SHA-224算法（生成224位/28字节哈希值）
export { SHA224, sha224 } from "./sha224.js";

// 导出SHA-256算法（生成256位/32字节哈希值）
// 这是目前使用最广泛的SHA-2变体，平衡了安全性和性能
export { SHA256, sha256 } from "./sha256.js";

// 导出SHA-384算法（生成384位/48字节哈希值）
// 此变体基于SHA-512，但使用不同的初始值并截断结果
export { SHA384, sha384 } from "./sha384.js";

// 导出SHA-512算法（生成512位/64字节哈希值）
// 最安全的SHA-2变体，使用64位操作，在64位处理器上可能比SHA-256更快
export { SHA512, sha512 } from "./sha512.js";

// 导出SHA-512/224算法（使用SHA-512的处理流程但截断至224位/28字节）
// 兼具SHA-512的内部结构和较短的输出长度
export { SHA512_224, sha512_224 } from "./sha512_224.js";

// 导出SHA-512/256算法（使用SHA-512的处理流程但截断至256位/32字节）
// 与SHA-256具有相同输出长度，但使用SHA-512的内部结构，在64位系统上可能更高效
export { SHA512_256, sha512_256 } from "./sha512_256.js";
