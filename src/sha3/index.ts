/**
 * SHA-3 哈希算法系列模块
 * 
 * SHA-3（Secure Hash Algorithm 3）是最新的哈希算法标准，由Keccak算法家族演变而来，
 * 于2015年由美国国家标准与技术研究院（NIST）正式发布。SHA-3与SHA-1和SHA-2使用完全不同的内部结构。
 * 
 * SHA-3的主要特点：
 * 1. 基于"海绵结构"（sponge construction）设计，与SHA-1和SHA-2完全不同
 * 2. 更高的安全性，能够抵抗针对SHA-1和SHA-2的攻击方法
 * 3. 可以产生任意长度的输出（通过SHAKE变体）
 * 4. 硬件实现效率更高
 * 
 * SHA-3包含两类算法：
 * - 固定输出长度的哈希函数：SHA3-224、SHA3-256、SHA3-384、SHA3-512
 * - 可扩展输出函数（XOF）：SHAKE128、SHAKE256
 * 
 * 本模块导出了所有SHA-3变体的类和便捷函数。
 */

// 导出固定长度的SHA-3哈希函数
export {
	// 函数形式 - 便于一次性计算哈希值
	sha3_224, // 生成224位（28字节）哈希值
	sha3_256, // 生成256位（32字节）哈希值
	sha3_384, // 生成384位（48字节）哈希值
	sha3_512, // 生成512位（64字节）哈希值
	
	// 类形式 - 支持流式处理大型数据
	SHA3_224, // SHA3-224的类实现
	SHA3_256, // SHA3-256的类实现
	SHA3_384, // SHA3-384的类实现
	SHA3_512  // SHA3-512的类实现
} from "./hash.js";

// 导出可扩展输出函数（XOF）
export { 
	// 函数形式 - 便于一次性生成哈希值
	shake128, // 基于128位安全强度的XOF
	shake256, // 基于256位安全强度的XOF
	
	// 类形式 - 支持流式处理大型数据
	SHAKE128, // SHAKE128的类实现
	SHAKE256  // SHAKE256的类实现
} from "./xof.js";
